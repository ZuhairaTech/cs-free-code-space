public class Practical1 {
  public static void main(String[] args) {
    // MULA
    int isnin = 120; // INPUT isnin
    int selasa = 80; // INPUT selasa
    int rabu = 100; // INPUT rabu
    int khamis = 90; // INPUT khamis
    int jumaat = 70; // INPUT jumaat
    int sabtu = 200; // INPUT sabtu
    
    int jumlah=isnin + selasa + rabu + khamis + jumaat + sabtu; // KIRA jumlah
        
    int purata=jumlah/6; // KIRA purata
        
    System.out.print(purata); // PAPAR purata
  }
}